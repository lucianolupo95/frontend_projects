const title = document.getElementById("title");
const author = document.getElementById("author");
const songImage = document.getElementById("song-image");
const sheet = document.getElementById("sheet");
songImage.onerror = function () {
  this.src = "assets/default-image.jpg";
};
const previousBtn = document.getElementById("previous-btn");
const nextBtn = document.getElementById("next-btn");
const showSheetBtn = document.getElementById("show-sheet-btn");
let sheetsShown = false;
let songIndex = 0;

function showSongByIndex(index) {
  title.innerText = songbook[index].title;
  author.innerText = songbook[index].author;
  songImage.src = songbook[index].image;
  showSheetBtn.innerText = "Ver notas";
  sheetsShown = false;
  if (songbook[index].sheet == "") {
    showSheetBtn.style.display = "none";
    sheet.innerText = "";
  } else {
    showSheetBtn.style.display = "initial";
  }
}

function randomiceSongbook() {
  songbook = shuffle(songbook);
  songIndex = 0;
  showSongByIndex(songIndex);
  previousBtn.classList.add("btn-disabled");
  nextBtn.classList.remove("btn-disabled");
}

function shuffle(array) {
  let currentIndex = array.length;
  while (currentIndex != 0) {
    let randomIndex = Math.floor(Math.random() * currentIndex);
    currentIndex--;
    [array[currentIndex], array[randomIndex]] = [
      array[randomIndex],
      array[currentIndex],
    ];
  }
  return array;
}

document
  .getElementById("randomize-btn")
  .addEventListener("click", randomiceSongbook);

previousBtn.addEventListener("click", () => {
  if (songIndex > 0) {
    if (songIndex == songbook.length - 1) {
      nextBtn.classList.remove("btn-disabled");
    }
    songIndex--;
    showSongByIndex(songIndex);
    if (songIndex == 0) {
      previousBtn.classList.add("btn-disabled");
    }
  }
});
nextBtn.addEventListener("click", () => {
  if (songIndex < songbook.length - 1) {
    if (songIndex == 0) {
      previousBtn.classList.remove("btn-disabled");
    }
    songIndex++;
    showSongByIndex(songIndex);
    if (songIndex >= songbook.length - 1) {
      nextBtn.classList.add("btn-disabled");
    }
  }
});
showSheetBtn.addEventListener("click", () => {
  if (!sheetsShown) {
    sheet.innerText = songbook[songIndex].sheet;
    showSheetBtn.innerText = "Ocultar notas";
    sheetsShown = true;
  } else {
    sheet.innerText = "";
    showSheetBtn.innerText = "Ver notas";
    sheetsShown = false;
  }
});

randomiceSongbook();
